// input output stream
#include <iostream>

using namespace std;
int main()
{

    int num1, num2;
    cout << "the val of num1 is: ";
    cin >> num1; //  '>>' is extraction operator
    cout << "the val of num2 is: ";
    cin >> num2; // '<<'  is insertion operator
    cout << "the sum is: " << num1 + num2 << endl;
    cout << "size of long double is:" << sizeof(long double);
    return 0;
    // pointer to pointer
}